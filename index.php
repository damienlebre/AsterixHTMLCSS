				<h1> Biographie </h1>
				<p>
                    Astérix est le fils d'Astronomix et Praline, deux Gaulois originaires du village dans lequel il vit actuellement et tenant un magasin de souvenirs à Condate. Il est né le même jour qu'Obélix, en 85 av. J.-C. (soit 15 ans après Jules César, 35 ans avant la conquête de la Gaule). Il est dans sa jeunesse le seul ami d'Obélix : la timidité et la corpulence d'Obélix en font un enfant exclu des autres, dont Astérix, premier de la classe, prend toujours la défense. Ils auraient à peu près le même âge que les autres personnages principaux du village à l'exception des plus âgés puisque Assurancetourix, Cétautomatix, Ordralfabétix, Abraracourcix et d'autres apparaissent comme leurs camarades dans l'album Comment Obélix est tombé dans la marmite du druide quand il était petit, ils étaient cependant plus âgés car déjà enfants lors de leur naissance. Un jour de bataille contre les Romains, Panoramix le druide assiste à la bataille, délaissant la surveillance de sa hutte. Astérix a alors une idée : faire boire un peu de potion magique à Obélix de façon qu'il prenne un peu de force et de confiance en lui. Les deux enfants se rendent ensemble discrètement dans la hutte du druide, et Astérix aide Obélix à se hisser sur le bord de la marmite. C'est à ce moment que Panoramix revient dans sa hutte. Astérix fait alors tomber Obélix dans la marmite, et réussit à sortir en douce de la hutte. Peu après, Panoramix ressort de sa hutte, tenant à bout de bras un Obélix qui a bu l'intégralité de la marmite de potion magique. C'est à ce jour, la seule « bêtise » connue d'Astérix. Depuis, son intelligence et sa ruse lui ont valu d'être le meilleur chasseur et guerrier du village, un des plus précieux conseillers d'Abraracourcix, et un bon confident de Panoramix.
                </p>
