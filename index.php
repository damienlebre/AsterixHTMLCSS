        <section id="from">
			<div class= "from-section">
				<div class="section-container">
					<h1> Le gaulois</h1>
					<div class="from-item">
						<img src="images/asterix.png">
						<p>D'après Astérix et la rentrée gauloise, Astérix serait né en 85 av. J.-C. (il aurait donc 35 ans, car les aventures d'Astérix se passent en 50 av. J.-C.). Son père, Astronomix et sa mère, Praline vivent à Condate (Rennes) où ils tiennent une boutique de souvenirs avec les parents d'Obélix, Obélodalix et Gelatine (voir Astérix et Latraviata). Il a aussi un cousin germain, Jolitorax, en Bretagne (voir Astérix chez les Bretons). Il reste célibataire mais un enfant bien que sa mère essaye de le « caser » dans Astérix et Latraviata. Cependant, au début de l'album L'anniversaire d'Astérix et Obélix - Le Livre d'or, alors qu'ils ont cinquante ans de plus, il est devenu grand-père.</p>
					</div>

                    <div class="from-item">
						<img src="images/asterix.png">
						<p>Le meilleur ami d'Astérix est sans conteste Obélix, qui l'accompagne dans toutes ses aventures (hormis la première, où il ne joue qu'un rôle très limité). À eux deux, ils sont les deux principaux défenseurs du village d'irréductibles. Astérix est le guerrier le plus rusé et le plus intelligent du village et prend souvent ses conseils chez le druide Panoramix, le sage du village qui prépare la fameuse potion magique. Il ne faut pas oublier Idéfix, le petit chien écologiste d'Obélix, qui est aussi un ami d'Astérix, bien que ce dernier ne veuille jamais l'emmener en voyage car il le considère comme trop petit (Idéfix apparaît dans Le tour de Gaule d'Astérix) . Astérix est aussi, comme Obélix, un ami du chef du village Abraracourcix.</p>
					</div>

                    <div class="from-item">
						<img src="images/asterix.png">
						<p>Astérix habite seul une hutte en face de celle d'Agecanonix. Après quelques hésitations sur son aspect extérieur (voir le premier album, Astérix le Gaulois), la hutte prend rapidement l'aspect illustré dans tous les autres albums : un toit en poutres, comme pour la hutte de Panoramix, une façade en pierre percée d'une porte et d'une fenêtre et un banc placé devant la hutte. Dans Le Fils d'Astérix, la hutte est équipée d'une sorte de mezzanine, où se trouve le lit d'Astérix. Obélix est souvent l'invité d'Astérix ("quand il s'ennuie" selon Astérix dans La Rose et le Glaive) et partage régulièrement le repas de son ami. En retour, Astérix dort parfois chez son ami lorsqu'il doit prêter sa maison à des visiteurs.</p>
