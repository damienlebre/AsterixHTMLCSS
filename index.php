			<div class="section-container">
				<h1> Description </h1>
				<ul>
                    <li>Il s'agit d'un personnage masculin de petite taille, blond, à moustache et avec — caractéristique des hommes de son village — un gros nez.</li>
                    <li>Il est toujours vêtu d'un gilet noir qui colle à la peau, de braies rouges et de chaussures. Il a aussi une épée courte, un glaive (dont il ne se sert presque jamais), et très souvent une petite gourde qui contient de la potion magique.</li>
                    <li>Il porte un casque doté d'ailes qui changent de position selon l'humeur du héros et dont il ne se sépare presque jamais.</li>
                </ul>
			</div>
